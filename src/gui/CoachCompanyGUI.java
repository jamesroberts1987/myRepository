    private void updateMaintenanceAddTaskActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateMaintenanceAddTaskActionPerformed
        String theDateString = updateMaintenanceTaskDate.getText();
        Date theDate = new Date(theDateString);

        String theTask = updateMaintenanceTask.getText();

        Coach theCoach = (Coach)updateMaintenanceCoachList.getSelectedValue();

        if(taskAndDateMap == null)
        {
            taskAndDateMap = theCoach.getMaintenanceLog().getMechanicTasksAndDates();
            for(Date eachDate : taskAndDateMap.keySet())
            {
                String eachDateString = formatter.format(eachDate);
                String aEntry = eachDateString + " - " + taskAndDateMap.get(eachDate);
                taskList.add(aEntry);
            }
        }

        taskAndDateMap.put(theDate, theTask);

        String taskAndDateString = theDateString + " - " + theTask;
        taskList.add(taskAndDateString);
        
        updateMaintenanceTaskList.setListData(taskList);
    }//GEN-LAST:event_updateMaintenanceAddTaskActionPerformed

