    private void addMaintenanceSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addMaintenanceSubmitActionPerformed
        Coach theCoach = (Coach)addMaintenanceCoachList.getSelectedValue();
        String theMileageString = addMaintenanceMileage.getText();
        int theMileage = Integer.parseInt(theMileageString);
        String theServiceDateString = addMaintenanceServiceDate.getText();
        Date theServiceDate = new Date(theServiceDateString);
        String theMotString = addMaintenanceMot.getText();
        Date theMotDate = new Date(theMotString);

        String result = coachCompany.addMaintenanceLog(theCoach, theMileage, theServiceDate, deficiencyList, theMotDate, taskAndDateMap);

        addMaintenanceOutcome.setText(result);

        noLogCoaches.clear();
        hasLogCoaches.clear();
        for(Coach eachCoach : coaches)
        {
            if(eachCoach.getMaintenanceLog() == null)
            {
                noLogCoaches.add(eachCoach);
            }
            else
            {
                hasLogCoaches.add(eachCoach);
            }
        }
        addMaintenanceCoachList.setListData(noLogCoaches);
        updateMaintenanceCoachList.setListData(hasLogCoaches);
        deficiencyList.clear();
        taskList.clear();
        taskAndDateMap.clear();

    }//GEN-LAST:event_addMaintenanceSubmitActionPerformed

    private void updateMaintenanceCoachButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateMaintenanceCoachButtonActionPerformed
        Coach theCoach = (Coach)updateMaintenanceCoachList.getSelectedValue();

        MaintenanceLog theLog = theCoach.getMaintenanceLog();

        int theMileage = theLog.getCurrentMileage();
        updateMaintenanceMileage.setText(Integer.toString(theMileage));
        Date theServiceDate = theLog.getServiceDate();
        String theServiceDateString = formatter.format(theServiceDate);
        updateMaintenanceServiceDate.setText(theServiceDateString);
        Date theMotDate = theLog.getMotDate();
        String theMotDateString = formatter.format(theMotDate);
        updateMaintenanceMot.setText(theMotDateString);

        List<String> theDeficiencies = theLog.getDeficiencies();
        updateMaintenanceDeficiencyList.setListData(theDeficiencies);

        HashMap<Date, String> theTaskAndDateMap = theLog.getMechanicTasksAndDates();

        ArrayList<String> taskAndDateList = new ArrayList<String>();

        for(Date eachDate : theTaskAndDateMap.keySet())
        {
            String theDateString = formatter.format(eachDate);
            String result = theDateString + " - " + theTaskAndDateMap.get(eachDate);
            taskAndDateList.add(result);
        }

        updateMaintenanceTaskList.setListData(taskAndDateList);
    }//GEN-LAST:event_updateMaintenanceCoachButtonActionPerformed

