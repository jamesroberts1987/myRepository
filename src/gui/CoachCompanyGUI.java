    private void addCoachSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addCoachSubmitActionPerformed
        String theRegistration = addCoachReg.getText();
        String theCapacityString = addCoachCap.getText();
        int theCapacity = Integer.parseInt(theCapacityString);
        String result = coachCompany.addCoach(theRegistration, theCapacity);
        coaches = coachCompany.getCoaches();
        addCoachOutcome.setText(result);
        Collection<Coach> updatedCoaches = coachCompany.getCoaches();
        removeCoachList.setListData(updatedCoaches);
        removeCoachList.setSelectedIndex(0);

        noLogCoaches.clear();
        hasLogCoaches.clear();
        for(Coach eachCoach : coaches)
        {
            if(eachCoach.getMaintenanceLog() == null)
            {
                noLogCoaches.add(eachCoach);
            }
            else
            {
                hasLogCoaches.add(eachCoach);
            }
        }
        addMaintenanceCoachList.setListData(noLogCoaches);
        updateMaintenanceCoachList.setListData(hasLogCoaches);
    }//GEN-LAST:event_addCoachSubmitActionPerformed

